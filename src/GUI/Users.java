    private void addActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addActionPerformed
        int p = JOptionPane.showConfirmDialog(null, 
                "ต้องกาารที่จะเพิ่มข้อมูลเจ้าใน records หรือไม่","เพิ่ม records"
                ,JOptionPane.YES_NO_OPTION);
        if(p==0){
            try {
                String sql = "insert into Users "
                        + "(Eid,username,password,department,level)"
                        + " values(?,?,?,?,?) ";
                pre=con.prepareStatement(sql);
                pre.setString(1, id.getText());
                pre.setString(2, username.getText());
                pre.setString(3, pass.getText());
                pre.setString(4, dep.getText());
                String selected = (String) level.getSelectedItem();
                pre.setString(5,selected);
                pre.execute();
                JOptionPane.showMessageDialog(null, 
                        "data have been saved");
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e);
            }
            finally{
                try {
                    res.close();
                    pre.close();
                } catch (Exception e) {
                }
            }           
        }
        Fill();
    }//GEN-LAST:event_addActionPerformed


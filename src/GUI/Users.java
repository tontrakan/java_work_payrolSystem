    private void Fill(){
    
        try {
            String sql = "select Eid,username,password from employees_info.Users ";
            pre=con.prepareStatement(sql);
            res=pre.executeQuery();
            while (res.next()) {
                String name = res.getString("username");
                level.addItem(name);
                                
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
        finally{
            try {
                res.close();
                pre.close();
            } catch (Exception e) {
            }
        }
        
    }

